const Task = require('../models/task');

const getAllTasks = async (req, res) => {
  //res.send("all items from the file");
  try {
    const tasks = await Task.find({});
    res.status(200).json({ tasks });
  } catch (err) {
    res.status(500).json({ msg: err });
  }
};

